    public function index(Request $request)
    {
        $user_id = $request->input('user_id');

        $select_user = null;
        if ($user_id) {
            $select_user = User::with('profile')->find($user_id);
        }

        $users = User::role('user')->with('profile')
        ->where('id','!=', Auth::id())
        ->get();

        return view('Employee.index', [
            'users' => $users,
            'select_user' => $select_user,
        ]);
    }




